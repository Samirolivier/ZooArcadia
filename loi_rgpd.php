    <h2><b>Le RGPD en France : Protection de vos données personnelles</b></h2>
    <blockquote class="blockquote">
      <hr></hr>
      <p><b>Qu'est-ce que le RGPD ?</b></p>
    </blockquote>
    <p>Le Règlement Général sur la Protection des Données (RGPD) est une réglementation européenne entrée en vigueur le 25 mai 2018, visant à renforcer la protection des données personnelles des citoyens de l’Union européenne. Ce règlement impose des obligations aux entreprises, administrations et organisations qui collectent, traitent ou stockent des données personnelles.

    En France, la mise en œuvre du RGPD est encadrée par la Commission Nationale de l'Informatique et des Libertés (CNIL), qui veille à la conformité des traitements de données avec la loi.</p>
    <hr></hr>
    <blockquote class="blockquote">
      <p><ul><li><u>Vos droits en tant qu'utilisateur</u></li></ul></p>
    </blockquote>
    <p>En vertu du RGPD, vous disposez de plusieurs droits concernant vos données personnelles. Voici vos principaux droits :</p>
    <ol>
      <li><b>Droit d'accès :</b> Vous avez le droit de savoir quelles données nous collectons, pourquoi et comment elles sont utilisées.</li>
      <li><b>Droit de rectification :</b> Vous pouvez demander la correction de vos données personnelles si elles sont inexactes ou incomplètes.</li>
      <li><b>Droit à l’effacement :</b> Vous pouvez demander la suppression de vos données dans certains cas (par exemple, si elles ne sont plus nécessaires).</li>
      <li><b>Droit à la portabilité :</b> Vous pouvez demander que vos données vous soient transmises ou transférées à une autre organisation.</li>
      <li><b>Droit d’opposition :</b> Vous avez le droit de vous opposer au traitement de vos données dans certaines situations, notamment à des fins de prospection commerciale.</li>
      <li><b>Droit à la limitation du traitement :</b> Vous pouvez demander que vos données soient temporairement "gelées" (non utilisées mais conservées).</li>
      <li><b>Droit de retrait du consentement :</b> Si vous avez donné votre consentement pour un traitement spécifique, vous pouvez le retirer à tout moment.</li>
    </ol>
